    private void tblPatientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPatientMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        if (model.getRowCount() != 0) {
            String city = model.getValueAt(tblPatient.getSelectedRow(), 0).toString();
            LoginDetails finddetailByCity = loginDetailDetailRepository.getDetailById(city);
            if (null != finddetailByCity) {
                lblId.setText(Long.toString(finddetailByCity.getId()));
                txtUserName.setText(finddetailByCity.getUsername());
                txtPassword.setText(finddetailByCity.getUserPassword());
                txtEmail.setText(finddetailByCity.getEmail());
                txtContact.setText(finddetailByCity.getContactNo());
                cmbRolesName.setSelectedItem(finddetailByCity.getRoleName());
            }
        }     // TODO add your handling code here:
    }//GEN-LAST:event_tblPatientMouseClicked

