    private void saveLbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveLbActionPerformed
         EmailValidator emailValidator = new EmailValidator();
        if (!emailValidator.validate(txtEmail.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Invalid Email !!");

        } else{
        LoginDetails network = new LoginDetails();
        network.setId(null);
        network.setUsername(txtUserName.getText());
        network.setUserPassword(txtPassword.getText());
        network.setRoleName(cmbRolesName.getSelectedItem().toString());
        network.setEmail(txtEmail.getText());
        network.setContactNo(txtContact.getText());
        loginDetailDetailRepository.save(network);
        JOptionPane.showMessageDialog(this, "USER Registered");
        }
    }//GEN-LAST:event_saveLbActionPerformed

