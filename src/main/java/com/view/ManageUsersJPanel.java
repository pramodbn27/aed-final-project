    private void loadUsers() {
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        model.setRowCount(0);
        List<LoginDetails> findAll = loginDetailDetailRepository.findAll();
        for (LoginDetails entity : findAll) {
            Object[] data = {entity.getUsername(),
                entity.getUserPassword(),
                entity.getRoleName(),
                entity.getEmail(),
               entity.getContactNo()};
            model.addRow(data);
        }
    }
